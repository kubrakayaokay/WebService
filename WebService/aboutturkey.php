<p style="color:white;">Although Turkey lags behind many countries in the space research activities, it is among the countries that are categorized according to certain abilities within the scope of space agencies and studies. These categories are; Human Flight to the Moon + Space Station + Manned Space Flight + Launch Capability + Extraterrestrial Administrations + Artificial Satellites. At this point, Turkey is in the category of "Artificial Satellites".

 Space exploration, which first started in the Cold War Era, continues to increase its economic, political and global importance with the increasing use of communication and internet today. Today's space exploration not only contributes to scientific research, but also contributes to many fields such as communication, image acquisition, tracking, tracking and location. The Space Technologies Research Institute (TÜBİTAK UZAY), which was the first to operate in Turkey, was established in 1985 with the aim of creating infrastructure and knowledge in the field of space. Afterwards, a certain budget is planned and the Turkish Space Agency is established in 2018 to conduct research and activities in the fields of space sciences, space exploration and the use of space.</p>

<br><br>

<p style="font-size:35px; color:white;">Turkey and Artificial Satellites</p>
<p style="color:white;">

Turkey's space projects continue to progress, mostly towards the satellite field. Turkey is in the category of "Artificial Satellites" within the scope of space capabilities. This category includes countries with satellites in space. Satellites are used for many purposes, from providing communication to sending images, from navigation systems to telescopic use. Turkey started its first space activities with the launch of Türksat 1B satellite, which it sent to the communication field in 1994. The satellites launched later are; Türksat 1C for direct connection between Europe and Central Asia in 1996, Türksat 2A covering Russia for TV purposes in 2001, Türksat 3A for high usage capacity communication and TV purposes in 2008, and for TV purposes covering China-England-Africa in 2014. There are Türksat 4A and Türksat 4B, which was sent for fast internet in 2015. Turkey's projects aimed to be launched for the future are; Türksat 5A, which will broadcast and transmit data in 3 continents in 2020, Türksat 5B, which will be used for comprehensive broadcasting produced with domestic means in 2021, and Türksat 6A, which will be the first national communication satellite in 2022.</p>


<br><br>

<p style="font-size:35px; color:white;">Turkey and Earth Observation Satellites</p>
<p style="color:white;">

Earth Observation Satellite is a type of satellite designed for non-military uses such as environmental monitoring, meteorology, map preparation, used for observation from Earth orbit. BİLSAT, Turkey's first earth observation and remote sensing satellite in 2003, served for the purposes of cartography, disaster monitoring, environmental monitoring and urban planning. Later, RASAT, the first earth observation satellite designed in Turkey in 2011, started its mission to collect higher resolution information.  </p>

<br><br>
<p style="font-size:35px; color:white;">Turkey and Discovery Satellites</p>
<p style="color:white;">

A reconnaissance satellite is a type of artificial satellite that includes enhanced military and intelligence hardware and applications, such as an earth observation satellite or communications satellite. The field of view of reconnaissance satellites is directed towards the earth, not towards space. It is mostly used for reconnaissance in many areas such as information retrieval, mapping and intelligence. Turkey first launched Göktürk-2 reconnaissance satellites in 2012 for the purpose of technological observation and research on space and satellite systems, and Göktürk-1 reconnaissance satellites in 2016 to obtain high resolution images for military intelligence purposes from any region on Earth. Among the reconnaissance satellites planned to be launched for the future is the Göktürk-3 project, which includes ultra-high resolution and enhanced hardware in 2020. </p>

<br><br>
<p style="font-size:35px; color:white;">Turkey and Space Studies</p>
<p style="color:white;">

Although Turkey has recently started to take steps in space studies, it is difficult to be among the leading countries in the field of space due to the lack of high technology and not making its investments in space so far. Turkey, which has just started space studies, is establishing the first Particle Radiation Test Infrastructure to test the components of satellites to be sent into space in order to reduce foreign dependency in satellite and space studies, and their resistance to radiation before the satellite is sent into space. The durability of solar cells, lithium-ion batteries and multi-layer insulation blankets of satellites will be tested according to the standards of the European Space Agency (ESA) within the scope of the project, which will be carried out in order to ensure that the satellites will not be affected by space radiation or shorten their lifetimes. In addition, within the scope of the project, it is aimed to develop a Native Radiation Monitor to be used in satellites. This monitor to be developed will enable the reporting of radiation effects on satellites.  </p>




